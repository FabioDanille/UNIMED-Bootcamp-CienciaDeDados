# Atribuição simples
saldo = 500
print(saldo)

# Atribuição com adição
saldo = 500
saldo += 200
print(saldo) # 700

# Atribuição com subtração
saldo = 500
saldo -= 200
print(saldo) # 300

# Atribuição com multiplicação
saldo = 500
saldo *= 2
print(saldo) # 1000

# Atribuição com divisão
saldo = 500
saldo /= 5
print(saldo) # 100

# Atribuição com módulo
saldo = 500
saldo %= 480
print(saldo) # 20

