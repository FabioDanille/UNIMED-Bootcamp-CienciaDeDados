# Inteiro para float
preco = 10
preco = float(preco)
print(preco)
print()

# Float para inteiro
preco = 10.30
preco = int(preco)
print(preco)
print()

# Conversão por divisão
preco = 10
print(preco / 2)
print(preco // 2)
print()

# Numérico para string
preco = 10.50
idade = 28
print(str(preco))
print(str(idade))
texto = f"idade {idade} preco {preco}"
print(texto)
print()

# String para número
preco = "10.50"
idade = "28"
print(float(preco))
print(int(idade))
print()

